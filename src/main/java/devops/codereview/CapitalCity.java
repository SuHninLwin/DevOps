package devops.codereview;

public class CapitalCity {
    //Variables for capital cities table in world database
    private String capCityName;
    private String capCityCountry;
    private String capCityPopulation;
    private String capCityContinent;
    private String capCityRegion;
    private String continentName;
    private String regionName;

    public String getCapCityName() {
        return capCityName;
    }

    public void setCapCityName(String capCityName) {
        this.capCityName = capCityName;
    }

    public String getCapCityCountry() {
        return capCityCountry;
    }

    public void setCapCityCountry(String capCityCountry) {
        this.capCityCountry = capCityCountry;
    }

    public String getCapCityPopulation() {
        return capCityPopulation;
    }

    public void setCapCityPopulation(String capCityPopulation) {
        this.capCityPopulation = capCityPopulation;
    }

    public String getCapCityContinent() {
        return capCityContinent;
    }

    public void setCapCityContinent(String capCityContinent) {
        this.capCityContinent = capCityContinent;
    }

    public String getCapCityRegion() {
        return capCityRegion;
    }

    public void setCapCityRegion(String capCityRegion) {
        this.capCityRegion = capCityRegion;
    }

    public String getContinentName() {
        return continentName;
    }

    public void setContinentName(String continentName) {
        this.continentName = continentName;
    }

    public String getRegionName() {
        return regionName;
    }

    public void setRegionName(String regionName) {
        this.regionName = regionName;
    }
}